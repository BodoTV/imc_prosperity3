class Strategy:
    def __init__(self, product: str, limit: int):
        self.product = product
        self.limit = limit

    @abstractmethod
    def act(self, state: TradingState) -> None:
        raise NotImplementedError()

    def run(self, state: TradingState) -> list[Order]:
        self.orders = []
        self.act(state)
        return self.orders

    def buy(self, price: int, quantity: int) -> None:
        self.orders.append(Order(self.product, int(price), quantity))

    def sell(self, price: int, quantity: int) -> None:
        self.orders.append(Order(self.product, int(price), -quantity))
    
    def convert(self, amount: int) -> None:
        self.conversions += amount

    def get_mid_price(self, state: TradingState, symbol: str) -> float:
        order_depth = state.order_depths[symbol]
        buy_orders = sorted(order_depth.buy_orders.items(), reverse=True)
        sell_orders = sorted(order_depth.sell_orders.items())

        popular_buy_price = max(buy_orders, key=lambda tup: tup[1])[0]
        popular_sell_price = min(sell_orders, key=lambda tup: tup[1])[0]

        return (popular_buy_price + popular_sell_price) / 2

    #this is for transferring data from one trader to the next
    def save(self) -> JSON:
        return None

    def load(self, data: JSON) -> None:
        pass

class BS_Signal(IntEnum):
    NEUTRAL = 0
    SHORT = 1
    LONG = 2

class Strategy_Signal(IntEnum):
    MM = 0
    TF = 1
    MR = 2
    BASKET = 3

class General_Strategy(Strategy):
    def __init__(self, 
                product: str, limit: int, 
                strategy_args):
        super().__init__(product, limit)
    
    @abstractmethod
    def get_bs_signal(self, state: TradingState) -> BS_Signal | None:
        raise NotImplementedError()
    
    @abstractmethod
    def get_strategy_signal(self, state: TradingState) -> Strategy_Signal | None:
        raise NotImplementedError()
    
    @abstractmethod
    def get_default_price(self, state: TradingState) -> int:
        raise NotImplementedError()
    
    def act(self, state: TradingState) -> None:
        new_signal = self.get_signal(state)
        if new_signal is not None:
            self.signal = new_signal

        position = state.position.get(self.symbol, 0)
        order_depth = state.order_depths[self.symbol]

        if self.signal == BS_Signal.NEUTRAL:
            if position < 0:
                self.buy(self.get_buy_price(order_depth), -position)
            elif position > 0:
                self.sell(self.get_sell_price(order_depth), position)
        elif self.signal == BS_Signal.SHORT:
            self.sell(self.get_sell_price(order_depth), self.limit + position)
        elif self.signal == BS_Signal.LONG:
            self.buy(self.get_buy_price(order_depth), self.limit - position)
    

    def get_buy_price(self, order_depth: OrderDepth) -> int:
        return min(order_depth.sell_orders.keys())

    def get_sell_price(self, order_depth: OrderDepth) -> int:
        return max(order_depth.buy_orders.keys())

    def save(self) -> JSON:
        return self.signal.value

    def load(self, data: JSON) -> None:
        self.signal = BS_Signal(data)
    
    
    